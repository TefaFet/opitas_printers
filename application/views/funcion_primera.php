<li><b>Sólido rendimiento y conectividad flexible:</b>Maneje trabajos desafiantes con un rendimiento de hasta 40 [38] páginas por minuto* y digitalización automática a una cara hasta 48 [46] imágenes por minuto. Conéctese mediante USB o Ethernet para otorgar acceso a todo el equipo.</li>
