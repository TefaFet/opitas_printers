<li><b>Imprima más y deténgase menos:</b>El tóner Unison™ de extra alto rendimiento disponible proporciona hasta 15 000 páginas** de salida, mientras que una bandeja de papel opcional aumenta la capacidad de entrada total hasta 900 hojas para obtener más impresiones y menos interrupciones. Además con su estructura de acero y un fusor de larga duración, la MX331adn está diseñada para durar.</li> 
